# 사용자로부터 여러 개의 숫자를 문자열로 입력 받아,
# 문자열을 개별 숫자로 분리하고, 각 숫자를 정수로 변환한 후,
# 모든 숫자의 합을 계산한다.
# 숫자의 총합이 100을 초과하면 해당 순간의 입력 값들과
# 총합을 출력하고 프로그램을 종료
# 숫자의 총합이 100을 초과하지 않은 경우 최종 총합과
# 입력된 숫자들을 출력하는 프로그램을 작성한다.

# 사용자로부터 여러 개의 숫자를 문자열로 입력 받는다.
numbers_str = input("숫자들을 쉼표로 구분하여 입력하세요: ")

# 문자열을 숫자 리스트로 변환한다.
numbers = numbers_str.split(',')

# 숫자 리스트의 개수
N = len(numbers)

# 각 숫자를 정수로 변환한 후, 모든 숫자의 합을 계산한다.
integer = list(map(int, numbers))

# 최종 총합 계산
sum1 = 0
sum2 = 0
for i in range(N):
    sum1 += integer[i]
    
# 현재까지의 총합 계산
for i in range(N-1):
    sum2 += integer[i]

# 숫자의 총합이 100을 초과하면 해당 순간의 입력 값들과
# 총합을 출력하고 프로그램을 종료한다.
if sum1 > 100:
    print("총합이 100을 초과하였습니다.")
    print(f"현재까지의 입력값들: {integer}")
    print(f"현재까지의 총합: {sum2}")

# 숫자의 총합이 100을 초과하지 않은 경우 최종 총합과
# 입력된 숫자들을 출력한다.
else:
    print("총합이 100을 초과하지 않았습니다.")
    print(f"입력된 모든 숫자들: {integer}")
    print(f"최종 총합: {sum1}")

    print(f"총합이 100을 초과하지 않았습니다.\n입력된 모든 숫자들: {integer}\n최종 총합: {sum1}")