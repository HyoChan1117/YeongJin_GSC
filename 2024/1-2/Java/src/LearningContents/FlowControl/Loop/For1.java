package LearningContents.FlowControl.Loop;

public class For1 {
    public static void main(String[] args) {

        for(int bar = 0 ; bar < 2 ; bar++) {
            System.out.println("Hello World!");
        }


        int foo = 0;
        while(foo < 2){
            System.out.println("Hello World!");
            foo++;
        }

    }
}
